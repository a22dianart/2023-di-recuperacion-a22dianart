    public boolean validarFormulario(){
        if("".equals(txtNome.getText())){
            return false;
        }
        try {
            int idade = Integer.parseInt(txtidade.getText());
            if (idade <= 0) {
                return false;
            } else {
                return true;
            }
        } catch (NumberFormatException e){
          return false;
           
        }
        
    }

